#This program prints a rhombus flower.

import turtle
wn = turtle.Screen()
wn.bgcolor("khaki")

tortle = turtle.Turtle()
tortle.shape("turtle")
tortle.fillcolor("green")
tortle.pencolor("black")
tortle.pensize(3)

tortle.forward(100)

tortle.left(45)
tortle.forward(100)

tortle.left(135)
tortle.stamp()
tortle.forward(100)

